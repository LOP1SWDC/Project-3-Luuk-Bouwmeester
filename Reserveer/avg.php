    <h1>Privacyverklaring voor reserveringen bij Hot Dasi Handi</h1>
    <p>Bij Hot Dasi Handi hechten we veel waarde aan de privacy van onze klanten. In deze privacyverklaring leggen we uit welke persoonlijke gegevens we verzamelen wanneer u een reservering maakt bij ons restaurant, hoe we deze gegevens gebruiken en beschermen. We streven ernaar om uw persoonlijke gegevens te allen tijde te beschermen en te voldoen aan de toepasselijke wet- en regelgeving met betrekking tot gegevensbescherming.</p>

    <h2>1. Verzamelde gegevens</h2>
    <p>Om uw reservering bij Hot Dasi Handi te kunnen verwerken, verzamelen we bepaalde persoonlijke gegevens, waaronder:</p>
    <ul>
        <li>Naam: We vragen uw volledige naam om uw reservering correct te kunnen noteren.</li>
        <li>Contactgegevens: We vragen uw telefoonnummer en/of e-mailadres zodat we u kunnen bereiken indien nodig, bijvoorbeeld om uw reservering te bevestigen of wijzigen.</li>
        <li>Datum en tijd van de reservering: We vragen om de gewenste datum en tijd van uw bezoek aan ons restaurant, zodat we uw reservering kunnen plannen.</li>
    </ul>

    <h2>2. Gebruik van gegevens</h2>
    <p>We gebruiken de verzamelde gegevens uitsluitend voor het verwerken en beheren van uw reservering bij Hot Dasi Handi. Dit omvat het noteren van uw reservering, het bevestigen van uw reservering en het eventueel contacteren van u in geval van wijzigingen in uw reservering.</p>

    <h2>3. Gegevensopslag en -beveiliging</h2>
    <p>We slaan uw persoonlijke gegevens op in ons beveiligde reserveringssysteem. We nemen redelijke maatregelen om ervoor te zorgen dat uw gegevens veilig worden bewaard en beschermd tegen ongeautoriseerde toegang, onthulling, wijziging of vernietiging.</p>

    <h2>4. Delen van gegevens</h2>
    <p>We delen uw persoonlijke gegevens niet met derden, tenzij dit nodig is om uw reservering te verwerken of wanneer we wettelijk verplicht zijn om dit te doen.</p>

    <h2>5. Bewaartermijn</h2>
    <p>We bewaren uw persoonlijke gegevens alleen zolang als nodig is om uw reservering te verwerken en eventuele vervolgacties uit te voeren. Na die periode zullen we uw gegevens veilig verwijderen.</p>

    <h2>6. Uw rechten</h2>
    <p>U heeft recht op toegang tot de persoonlijke gegevens die we over u hebben verzameld en het recht om deze gegevens te corrigeren, bij te werken of te verwijderen. Als u uw gegevens wilt opvragen, corrigeren, bijwerken of verwijderen, neem dan contact met ons op via de contactgegevens aan het einde van deze privacyverklaring.</p>

    <h2>7. Wijzigingen in de privacyverklaring</h2>
